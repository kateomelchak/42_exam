#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	char c;

	i = 0;
	if (argc < 2)
		write(1, "\n", 1);
	else
	{
		while (argv[1][i] != '\0')
		{
			c = argv[1][i];
			write(1, &c, 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
